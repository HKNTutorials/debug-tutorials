// Compute Fibonacci numbers. Define fibonacci(0) == 0, fibonacci(1) == 1 and
// the rest according to fibonacci(n) = fibonacci(n - 1) + fibonacci(n - 2).

// recursive implementation
int fibonacciRecursive(int n);

// linear-time iterative implementation
int fibonacciIterative(int n);

// use a function to directly compute the nth Fibonacci number
int fibonacciClosedForm(int n);

// take the exponent of a particular matrix efficiently to compute Fibonacci
// numbers
int fibonacciExponent(int n);
